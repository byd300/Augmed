	<h3>USG A, USG B, Pachymetria</h3>
		<img src="images/underline.png" alt="inderline" class="underline">
		<span>USG to badanie, które wykorzystuje ultradźwięki do obrazowania wewnętrznych struktur oka.<br><br>

		<h6>USG A</h6>
		to badanie USG którego celem jest pomiar długości gałki ocznej. Najczęściej wykonywane w ramach kalkulacji mocy soczewki wewnątrzgałkowej przed operacją zaćmy. Wymaga podania kropli znieczulających do oka, gdyż w przebiegu badania dochodzi do bezpośredniego kontaktu cienkiej głowicy aparatu z gałką oczną (szczytem rogówki).<br><br>

		<h6>USG B</h6>
		to badanie USG które pozwala ocenić wewnętrzne struktury gałki ocznej zwłaszcza przy ograniczonym wglądzie w dno oka. Wykonywane jest za pomocą grubszej głowicy niż USG A, przez powieki, z zastosowaniem specjalnego żelu poprawiającego jakość badania.<br><br>

		Pachymetria
		to badanie mające na celu ocenę grubości rogówki. Można tego dokonać za pomocą specjalnej sondy USG. Przebieg badania jest podobny jak USG A, osoba badająca delikatnie dotyka powierzchni rogówki osoby badanej po uprzednim podaniu kropli znieczulających do worka spojówkowego.<br><br></span>

		<h6>Wskazania </h6>
		<ul>
			<li>ocena budowy gałki ocznej,</li>
			<li>obrazowanie siatkówki przy braku wglądu w dno oka (np. dojrzała zaćma, wylew krwi do ciała szklistego, podejrzenie odwarstwienia siatkówki)</li>
		</ul>
